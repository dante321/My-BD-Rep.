  CREATE OR REPLACE FORCE VIEW "LA_LIGA"."JUGADORES_VIEW" ("NOMBRE", "EQUIPO_ID", "SALARIO") AS 
  SELECT 
    JUGADORES.NOMBRE,    
    EQUIPO.EQUIPO_ID,
    JUGADORES.SALARIO
FROM
    JUGADORES,
    EQUIPO
WHERE 
    JUGADORES.SALARIO>3000;
